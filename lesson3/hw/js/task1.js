// 2 reverse 

var myArray = ["Banana", "Orange", "Apple", "Mango"];
var reverseMyArray = myArray.reverse();
console.log(reverseMyArray);

// 3 sort

var myArray = ["Banana", "Orange", "Apple", "Mango"];
var sortMyArray = myArray.sort();
console.log(sortMyArray);

// 4 concat

var myArray = ["Banana", "Orange", "Apple", "Mango"];
var concatMyArray = myArray.concat(["Fruit"]);
console.log(concatMyArray);

// 5 slice

var myArray = ["Banana", "Orange", "Apple", "Mango"];
